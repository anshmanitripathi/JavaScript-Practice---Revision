// push to add at end
// pop to delete from end and returns it


let arr = ["ji", "g", 23.76, 67, ];

console.log(arr);

arr.push("happy");
arr.push("hi");
arr.push("hey");

console.log(arr);

arr.pop();
console.log(arr);

//  unshift()  add element to start
//  shift()  delete element from start and return it

arr.unshift("AAAA");
arr.unshift("BBBBB");
arr.unshift("CCCCC");

console.log(arr);
arr.shift();
console.log(arr);


